class Context {

    private String data;
    private String secret;

    public void setData(String data) {
        this.data = data;
    }

    public String getData() {
        return data;
    }

    public void setSecret(String secret) {
        this.secret = secret;
    }

    public String getSecret() {
        return secret;
    }
}
